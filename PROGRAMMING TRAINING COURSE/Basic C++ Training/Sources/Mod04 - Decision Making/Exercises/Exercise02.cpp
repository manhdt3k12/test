/* Example: Check Prime Number */
#include <iostream>
using namespace std;

int Exercise02()
{
  int n, i;
  bool isPrime = true;

  cout << "Enter a positive integer: ";
  cin >> n;

  for(i = 2; i <= n / 2; ++i)
  {
      if(n % i == 0)
      {
          isPrime = false;
          break;
      }
  }
  if (isPrime)
      cout << "This is a prime number";
  else
      cout << "This is not a prime number";

  return 0;
}
/* Output */
//- Enter a positive integer: 29
//- This is a prime number.
