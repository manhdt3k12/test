// C++ program to demonstrate ceil function 
#include <iostream> 
#include <cmath> 
using namespace std; 
  
// Driver function 
int Ceil_and_Floor_02() 
{ 
    // using ceil function which return 
    // floor of input value 
    cout << " Ceil is : " << ceil(2.3) << endl; 
    cout << " Ceil is : " << ceil(-2.3) << endl; 
  
    return 0; 
} 
/* Output *//*
Ceil is : 3
Ceil is : -2
*/

