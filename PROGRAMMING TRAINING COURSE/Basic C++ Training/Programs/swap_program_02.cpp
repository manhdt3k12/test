#include <bits/stdc++.h> 
using namespace std; 
  
int swap_program_02() 
{ 
    string a = "IMIC"; 
    string b = "Technology"; 
    cout << "Value of a before: " << a << endl; 
    cout << "Value of b before: " << b << endl; 
    swap(a, b); 
    cout << "Value of a now: " << a << endl; 
    cout << "Value of b now: " << b << endl; 
    return 0; 
} 
/* Output *//*
Value of a before: IMIC
Value of b before: Technology
Value of a now: Technology
Value of b now: IMIC
*/
